class reactangle 
{
    public static void main(String[] args) 
    {
        int n = 3;
        int n1 = 3;
        for(int i=1;i<=n;i++)
        {
            for(int j=0;j<=n1;j++)
            {
                System.out.print(" ");
                System.out.print("*");
            }
            System.out.println();
        }

    }
}
